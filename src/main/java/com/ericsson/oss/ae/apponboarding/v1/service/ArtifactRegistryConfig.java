package com.ericsson.oss.ae.apponboarding.v1.service;

import org.springframework.beans.factory.FactoryBean;
import org.springframework.beans.factory.config.ServiceLocatorFactoryBean;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.retry.annotation.EnableRetry;

@Configuration
@EnableRetry
public class ArtifactRegistryConfig {

    @Bean("artifactRegistryFactory")
    public FactoryBean serviceLocatorFactoryBean() {
        final ServiceLocatorFactoryBean factoryBean = new ServiceLocatorFactoryBean();
        factoryBean.setServiceLocatorInterface(ArtifactRegistryFactory.class);
        return factoryBean;
    }

}
